package com.control.statement;

public class PrePostIncrement {
	
	public static void main(String[] args) {
		
		int i = 18;
		
		System.out.println(++i);
		System.out.println(--i);
		System.out.println(++i);
		System.out.println(--i);
		System.out.println(++i);
		System.out.println(--i);
		System.out.println(++i);
		System.out.println(--i);
		System.out.println(++i);
		System.out.println(--i);
		System.out.println(++i);
		System.out.println(++i);
		System.out.println(i++);
		
		System.out.println(i);
	}

}
